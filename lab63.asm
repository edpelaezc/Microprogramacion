.MODEL SMALL
.STACK
.DATA
	MSG1 DB 10,13,'Ingrese la Cadena:  $'

	MSG2 DB 10,13,'La cadena es Palidromo$'

	MSG3 DB 10,13,'La cadena no es Palidromo$'

	NEW DB 10,13,'$'

	INST DB 20 DUP(0)

.CODE    
           
PROGRAMA:
	MOV AX,@DATA
	MOV DS,Ax
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	MOV BX,00

ARRIBA:

	MOV AH,01H
	INT 21H
	CMP AL,0DH
	JE ABAJO
	MOV [INST+BX],AL
	INC BX
	LOOP ARRIBA
ABAJO:

	MOV DI,0
	DEC BX
	JNZ CHECK
CHECK:

	MOV AL,[INST+BX]
	CMP AL,[INST+DI]
	JNE FALLO
	INC DI
	DEC BX
	JNZ CHECK
	LEA DX,NEW
	MOV AH,09H
	INT 21H
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	JMP FINAL

FALLO:

	LEA DX,MSG3
	MOV AH,09H
	INT 21H
FINAL:

	INT 3
	
END PROGRAMA

END